import pyautogui as mouse
import time

while True:
    mouse.moveTo(100, 100, duration=2)
    time.sleep(5)
    mouse.moveTo(200, 100, duration=2)
    time.sleep(5)
    mouse.moveTo(200, 200, duration=2)
    time.sleep(5)
    mouse.moveTo(100, 200, duration=2)
    time.sleep(5)
    mouse.moveTo(100, 200, duration=2)
